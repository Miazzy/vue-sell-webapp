<template>
  <transition name="move">
    <div class="food" v-show="showFlag" ref="food">
      <div class="food-content">
        <div class="image-header">
          <img :src="food.image" alt="">
          <div class="back" @click="hide">
            <i class="icon-arrow_lift"></i>
          </div>
        </div>

        <div class="content">
          <h1 class="title">{{food.name}}</h1>
          <detail :food="food" />
          <div class="cartcontrol-wrapper">
            <cartcontrol @add="addFood" :food="food"></cartcontrol>
          </div>
          <transition name="fade">
            <div class="buy" v-show="!food.count || food.count === 0" @click.stop.prevent="addFirst">加入购物车</div>
          </transition>
        </div>

        <split v-show="food.info"/>

        <div class="info">
          <h1 class="title">商品信息</h1>
          <p class="text">{{food.info}}</p>
        </div>

        <split />

        <div class="rating">
          <h1 class="title">商品评价</h1>

          <ratingselect :ratings="food.ratings" :selectType="selectType" :onlyContent="onlyContent" :desc="desc" @select="selectRating" @toggle="toggleContent"/> 

          <div class="rating-wrapper">
            <ul v-show="food.ratings && food.ratings.length">
              <li class="rating-item border-1px" v-for="(rating,index) in food.ratings" :key="index" v-show="needShow(rating.rateType,rating.text)">
                <div class="user">
                  <span class="name">{{rating.username}}</span>
                  <img :src="rating.avatar" alt="" class="avatar" width="12" height="12">
                </div>
                <div class="time">{{rating.rateTime | formatDate}}</div>
                <div class="text">
                  <span :class="{'icon-thumb_up': rating.rateType === 0, 'icon-thumb_down':rating.rateType === 1}"></span>{{rating.text}}
                </div>
              </li>
            </ul>

            <div class="no-rating" v-show="!food.ratings || !food.ratings.length">暂无评价</div>
          </div>
        </div>
      </div>
    </div>
  </transition>
</template>

<script>
  import detail from "components/detail/detail";
  import BScroll from 'better-scroll';
  import Vue from 'vue';
  import cartcontrol from "components/cartcontrol/cartcontrol";
  import split from "components/split/split";
  import ratingselect from "components/ratingselect/ratingselect";
  import { formatDate } from "common/js/date.js";

  const ALL = 2

  export default {
    props: {
      food: {
        type: Object
      }
    },
    data() {
      return {
        showFlag: false,
        selectType: ALL,
        onlyContent:true,
        desc: {
          all: '全部',
          positive: '推荐',
          negative: '吐槽'
        }
      }
    },
    components: {
      detail,
      cartcontrol,
      split,
      ratingselect
    },
    methods: {
      show() {
        this.showFlag = true
        this.selectType = ALL
        this.onlyContent = true
        this.$nextTick(() => {
          if(!this.scroll) {
            this.scroll = new BScroll(this.$refs.food, {
              click: true
            })
          }else{
            this.scroll.refresh()
          }
        })
      },
      hide() {
        this.showFlag = false
      },
      addFood(target) {
        this.$emit('add', target);
      },
      addFirst(event) {
        if(!event._constructed) {
          return
        }
        this.$emit('add',event.target)
        Vue.set(this.food, 'count', 1)
      },

      selectRating(type) {
        this.selectType = type
        this.$nextTick(()=>{
          this.scroll.refresh()
        })
      },
      toggleContent() {
        this.onlyContent = !this.onlyContent
        this.$nextTick(()=>{
          this.scroll.refresh()
        })
      },
      needShow(type,text) {
        if(this.onlyContent && !text) {
          return false
        }
        if(this.selectType === ALL){
          return true
        }else{
          return type === this.selectType
        }
      }
    },
    filters: {
      formatDate(time) {
        let date = new Date(time)
        return formatDate(date, 'yyyy-MM-dd hh:mm')
      }
    }
  }
</script>

